     */
    public function store(Request $request)
    {



       
        $pro = new User();
        $pro->name = $request->get('name');
        $pro->email = $request->get('email');
        $pro->password = Hash::make($request->get('password'));
        $pro->is_admin = '2';
        $pro->save();
        

        return redirect()->back()->with('massage', 'success');
    }

